package operadores;

public class Relacional {
	public static void main(String[] args) {

		/*
		 * < - menor que
		 * > - maior que
		 * <= - menor ou igual que
		 * >= - maior ou igual que
		 * == - igual a
		 * != - diferente de
		 * 
		 * 
		 * */
			
		int num1 = 5;
		int num2 = 7;
		
		boolean resultado = num1 > num2;
		System.out.println(resultado);
		
		resultado = num1 < num2;
		System.out.println(resultado);
		
		resultado = num1 >= num2;
		System.out.println(resultado);
		
		resultado = num1 <= num2;
		System.out.println(resultado);
		
		resultado = num1 == num2;
		System.out.println(resultado);
		
		resultado = num1 != num2;
		System.out.println(resultado);
		
		
		
		
		
		
	}
}
