package lacos_de_repeticao;

public class LacoFor {

	public static void main(String[] args) {
		// Dois tipo de escopo
		// Global e Local

		for(int i = 1;i <= 10;i++) {
			System.out.print(i + " ");
		}
		
		System.out.println("\n----------------");
		
		for(int y = 10; y >= 1; y--) {
			System.out.print(y + " ");
		}

		
	}

}
